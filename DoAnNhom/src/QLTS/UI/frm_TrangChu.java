    public String LayGio()
    {
        LocalDateTime now = LocalDateTime.now();
        int year = now.getYear();
        int month = now.getMonthValue();
        int day = now.getDayOfMonth();
        int hour = now.getHour();
        int minute = now.getMinute();
        int second = now.getSecond();
        String gio = day+"/"+month+"/"+year+" "+hour+":"+minute+":"+second;
        return gio;
    }
    public static void sortTableByColumn(JTable table, int columnIndex) {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        Vector dataVector = model.getDataVector();

        Collections.sort(dataVector, new Comparator<Vector>() {
            @Override
            public int compare(Vector o1, Vector o2) {
                Comparable c1 = (Comparable) o1.get(columnIndex);
                Comparable c2 = (Comparable) o2.get(columnIndex);
                return c1.compareTo(c2);
            }
        });

        model.fireTableDataChanged();
    }
