    private void Pl_BaoCaoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Pl_BaoCaoMouseClicked
        // TODO add your handling code here:
        
        cardLayout.show(MenuChinh, "pnlQL_BaoCao");
        fillToTable_BaoCao();
        int totalTien = 0;
        int totalHD = 0;
        int totalLy = 0;
        for (int row = 0; row < tabBaoCao.getRowCount(); row++) {
            int ly = Integer.parseInt(tabBaoCao.getValueAt(row, 1).toString());
            int hd = Integer.parseInt(tabBaoCao.getValueAt(row, 2).toString());  
            int tien = Integer.parseInt(tabBaoCao.getValueAt(row, 3).toString()); 
            totalTien += tien;
            totalHD += hd;
            totalLy += ly;
        }
        txtTongHoaDon.setText(String.valueOf(totalHD));
        txtTongThanhTien.setText(String.valueOf(totalTien));
        txtTongLy.setText(String.valueOf(totalLy)+" Ly");
    }//GEN-LAST:event_Pl_BaoCaoMouseClicked

