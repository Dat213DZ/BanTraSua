    private static void copyFile(File sourceFile) {
        try {
            // Thư mục đích trong dự án để lưu tệp đã sao chép
            String destinationDirectory = "src\\QLTS\\IMG\\DoUong"; // Thay đổi đường dẫn này

            // Tạo đối tượng Path cho thư mục đích
            Path destinationPath = Path.of(destinationDirectory);

            // Tạo đối tượng Path cho tệp đích trong thư mục đích
            Path destinationFilePath = destinationPath.resolve(sourceFile.getName());

            // Sao chép tệp từ nguồn đến đích
            Files.copy(sourceFile.toPath(), destinationFilePath, StandardCopyOption.REPLACE_EXISTING);

            System.out.println("File copied successfully.");
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
