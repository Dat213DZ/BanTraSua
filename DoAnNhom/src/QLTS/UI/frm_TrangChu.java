    public String LoadNhanVien(String Manv)
    {
        String TenNV = "";
        NhanVienDAO nvDAO = new NhanVienDAO();
        listNV = nvDAO.docTatCaNhanVien();
        for(NhanVien nv: listNV)
        {
            if(nv.getMaNV().equals(Manv))
            {
                TenNV = nv.getTenNV();
                break;
            }
            else
            {
                TenNV = "Trống";
            }
        }
        return TenNV;
    }
