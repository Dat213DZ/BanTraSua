        JFileChooser chon = new JFileChooser();
        String diachi = "src:\\QLTS\\IMG\\DoUong";
        chon.setCurrentDirectory(new File(diachi));
        FileNameExtensionFilter filter = new FileNameExtensionFilter("images", "jpg", "gif", "png", "jpeg");
        chon.setFileFilter(filter);
        int resutl = chon.showOpenDialog(null);
        if (resutl == JFileChooser.APPROVE_OPTION) {
            String filePath = chon.getSelectedFile().getAbsolutePath();
            File selectedFile = chon.getSelectedFile();
            imgPath = chon.getSelectedFile().getName();
            copyFile(selectedFile);
            loadHinh(filePath);
            txtHinhAnh.setText(imgPath);
        }
